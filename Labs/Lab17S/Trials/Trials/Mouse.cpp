#include "Mouse.h"
#include "Item.h"
#include "Trials.h"

// ---------------------------------------------------------------------------------

Mouse::Mouse()
{
	x = window->MouseX();
	y = window->MouseY();
	bbox = new Point(x, y);
}

// ---------------------------------------------------------------------------------

Mouse::~Mouse()
{
	delete bbox;
}

// -------------------------------------------------------------------------------

void Mouse::Update()
{
	MoveTo(window->MouseX(), window->MouseY());
}

// -------------------------------------------------------------------------------

void Mouse::Draw()
{
}

// -------------------------------------------------------------------------------

bool Mouse::Clicked()
{
	if (window->KeyCtrl(VK_LBUTTON))
		return true;
	else
		return false;
}

